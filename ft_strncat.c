#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	char *d;

	d = dest + ft_strlen(dest);
	while (n > 0 && *src)
	{
		*d++ = *src++;
		n--;
	}
	if (*src != '\0' || n > 0)
		*d = '\0';
	return (dest);
}
